// 5. Create a function that reverses an array.
const myArray = ['Monday', 'Tuesday', 'Wednessday', 'Thursday', 'Friday', 'Saturday', 'Sunday']

function reverseArray(myArray){
    return myArray.reverse();
}

console.log(myArray);       //Normal Array
console.log(reverseArray(myArray));     //reversed Array
